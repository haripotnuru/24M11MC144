public class IncreDecre {
    public static void main(String[] args) {
        int num1=12;
        int num2=23;
        //Using unary operator Incremantation 
        System.out.println("post_increment of num1:"+(num1++));
        System.out.println("pre_increment of num1:"+(++num1));
        //   //Using unary operator decremantation 
        System.out.println("post_decrement of num1:"+(num2--));
        System.out.println("pre_increment of num1:"+(--num2));
    }
}
