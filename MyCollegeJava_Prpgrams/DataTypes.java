public class DataTypes {
    public static void main(String[] args) {
        //All data types
        // Charactor data type
        char ch='H';
        // Byte data type declaration
        byte by= 23;
        // Short data type declaration
        short sh= 345;
        // Integer data type declaration
        int in=3456;
        // long data type declaration
        long ln= 234555555;
        // Float data type declaration
        float fl=2.35698543433555f;
        // double data type declaration
        double db= 6.9865765765;
        //Printing the values 
        System.out.println("char:"+ch);
        System.out.println("Btye:"+by);
        System.out.println("Short:"+sh);
        System.out.println("Integer:"+in);
        System.out.println("Long:"+ln);
        System.out.println("Float:"+fl);
        System.out.println("Double:"+db);
    }
}
