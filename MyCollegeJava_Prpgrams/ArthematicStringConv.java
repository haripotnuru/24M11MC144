public class ArthematicStringConv {
    public static void main(String[] args) {
        // Assigning the integer value to string
        String s1="35";
        String s2="23";
        //convert string into integer method called parseInt()
        int numb1=Integer.parseInt(s1);
        int numb2=Integer.parseInt(s2);
        System.out.println("num1 and num2 :"+numb1+","+numb2);
    }
}
